public class ThreeOperate {
	public static void main(String[] args) {
		/**
		 * 三元运算符
		 */
		int a = 30;
		int b = 50;
		int g = 40;
		int e = 60;
		int d = (a > b ? g : e);
		System.out.println(d);
	}
}

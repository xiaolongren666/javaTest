public class OneOperate {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int a = 4;
		// 一元运算符
		
		int b = ++a;
		System.out.println("b的值:"+b);
		System.out.println("a的值:"+a);
		
		b = --a;
		System.out.println("b的值:"+b);
		System.out.println("a的值:"+a);
	}

}
